class calculator:
    @staticmethod
    def subtraction(a, b):
        out = a - b
        print(out)

    @staticmethod
    def addition(a, b):
        out = a + b
        print(out)

    @staticmethod
    def division(self, a, b):
        out = a / b
        print(out)

    @staticmethod
    def multiplication(a, b):
        out = a * b
        print(out)

calculator.multiplication(2, 3)
